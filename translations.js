// Dil deƒüi≈ütirme fonksiyonu
function changeLanguage(lang) {
    const elements = document.querySelectorAll('[id]');
    elements.forEach(element => {
        const key = element.id;
        if (translations[lang] && translations[lang][key]) {
            let text = translations[lang][key];
            
            // Skor placeholder'ƒ±nƒ± deƒüi≈ütir
            if (text.includes('{score}')) {
                const currentScore = element.textContent.match(/\d+/);
                if (currentScore) {
                    text = text.replace('{score}', currentScore[0]);
                }
            }
            
            element.textContent = text;
        }
    });
    
    // Dil se√ßiciyi g√ºncelle
    document.getElementById('languageSelect').value = lang;
    
    // Local storage'a kaydet
    localStorage.setItem('selectedLanguage', lang);
}

// Sayfa y√ºklendiƒüinde dil ayarƒ±nƒ± y√ºkle
document.addEventListener('DOMContentLoaded', () => {
    const savedLanguage = localStorage.getItem('selectedLanguage') || 'tk';
    changeLanguage(savedLanguage);
    
    // Dil se√ßici event listener
    document.getElementById('languageSelect').addEventListener('change', (e) => {
        changeLanguage(e.target.value);
    });
}); 